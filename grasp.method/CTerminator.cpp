#include "CTerminator.h"

//----------------------------------------------------------------------------------
// 
//----------------------------------------------------------------------------------
StopByIteration::StopByIteration(unsigned long it)
{
	n_iterations = it;
}

//----------------------------------------------------------------------------------
// 
//----------------------------------------------------------------------------------
StopByIteration::~StopByIteration(void)
{
}

//----------------------------------------------------------------------------------
// 
//----------------------------------------------------------------------------------
bool StopByIteration::isDone(const CStatistics &stats)
{
	return stats.getIterations() >= n_iterations;
}

//----------------------------------------------------------------------------------
// 
//----------------------------------------------------------------------------------
StopByTarget::StopByTarget(float tgt)
{
	target = tgt;
}

//----------------------------------------------------------------------------------
// 
//----------------------------------------------------------------------------------
StopByTarget::~StopByTarget(void)
{
}

//----------------------------------------------------------------------------------
// 
//----------------------------------------------------------------------------------
bool StopByTarget::isDone(const CStatistics &stats)
{
	return stats.getBestScore() <= target;
}

//----------------------------------------------------------------------------------
// 
//----------------------------------------------------------------------------------
StopByTime::StopByTime(double t)
{
	time = t;
}

//----------------------------------------------------------------------------------
// 
//----------------------------------------------------------------------------------
StopByTime::~StopByTime(void)
{
}

//----------------------------------------------------------------------------------
// 
//----------------------------------------------------------------------------------
bool StopByTime::isDone(const CStatistics &stats)
{
	return stats.getElapsedTime() >= time;
}
